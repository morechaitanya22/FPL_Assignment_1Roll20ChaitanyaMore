#include <stdio.h>

int main() {
    int a = 20, b = 5;

    printf("a = %d, b = %d\n\n", a, b);

    // Addition
    printf("a + b = %d\n", a + b);

    // Subtraction
    printf("a - b = %d\n", a - b);

    // Multiplication
    printf("a * b = %d\n", a * b);

    // Division
    printf("a / b = %d\n", a / b);

    // Modulus
    printf("a %% b = %d\n", a % b);

    // Modulus gives us the remainder that is left after division.
    
    return 0;
}